				<p><br>
					Mark Lynch, aka &Eacute;clat, has been writing poetry since the age of eight and was born in the same state of the Union that produced Georgia O'Keefe, Orson Welles, Gene Wilder, Thornton Wilder and Frank Lloyd Wright: <i>Wisconsin</i>.&nbsp;</p>
				<p>As an English student of Wisconsin poet and small press advocate, John Judson, Mark did the cover photography of Judson's <i>Routes from the Onion's Dark</i>, winner of&nbsp; the 1976 Wisconsin Poetry Book of the Year.</p>
				<p>But it was only after a successful career in public television that Mark turned to poetry, specifically poetic caricatures about people's personalities, in the late 1990's.&nbsp;</p>
				<p>Mark drew his inspiration from his youth, when he was taken by his father to view twelve rooms of paintings and drawings by Toulouse-Lautrec at the Art Institute of Chicago. Lautrec&rsquo;s work made art celebratory for Mark. Much more intimate than the mass-produced Moulin-Rouge posters, each piece depicted a person or group. No still-lifes or landscapes. Only personalities.</p>
				<p>Mark Lynch, utilizing the pseudonym &Eacute;clat, and inspired by the art of Toulouse-Lautrec, now offers drawings of personalities&#x2026;with words.</p>
